public class Point {

    private int i;
    private int j;

    public Point() {
        i = 0;
        j = 0;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public void setI(int i) {
        this.i = i;
    }

    public void setJ(int j) {
        this.j = j;
    }
}
